class IncrementOperatorExample{
public static void main (String[] args){
System.out.println("***********pre increment operations ******************");
int m=7;
int n=++m;
System.out.println("m:"+m);
System.out.println("n:"+n);

System.out.println("******post increment operations******");
int p=50;
int q=p++;
System.out.println("p:"+p);
System.out.println("q:"+q);
}
}
